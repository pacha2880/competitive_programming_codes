void init(int b, int e, int nodo)
{
    if(b == e)
    {
        t[nodo] = {ar[b], ar[b]};
        return;
    }
    index;
    init(b, mid, l);
    init(mid + 1, e, r);
    t[nodo] = join(t[l], t[r]);
}
node query(int b, int e, int nodo, int i, int j)
{
    if(b >= i && e <= j)
        return t[nodo];
    index;
    if(mid >= j)
        return query(b, mid, l, i, j);
    if(mid < i)
        return query(mid + 1, e, r, i, j);
    return join(query(b, mid, l, i, j), query(mid + 1, e, r, i, j));
}
