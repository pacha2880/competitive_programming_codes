int f(int pozo, int num)
{
    if(num == limi) return 0;
    if(dp[pozo][num] != -1) return dp[pozo][num];
    return dp[pozo][num] = max(f(pozo, nex[pozo][num]), 1 + f(pozo ^ 1, nex[pozo ^ 1][num]));
}
