bool comp(int b1, int e1, int b2, int e2){ 
  int siz = min(e1 - b1, e2 - b2);
  ii le = query(b1, b1 + siz), ri = query(b2, b2 + siz);
  if(le == ri)
    return e1 - b1 == e2 - b2 ? e1 > e2 : e1 - b1 < e2 - b2;
  return le < ri;
}
