int trabuco[3 * tam];
void tur(int node, int pa)
{
    posas[node].insert(con);
    trabuco[con] = node;
    inas[node] = con;
    tururu.pb(con);
    con++;
    for(int x : g[node])
    {
        if(x != pa)
        {
            tur(x, node);
            posas[node].insert(con);
            tururu.pb(inas[node]);
            con++;
        }
    }
}
