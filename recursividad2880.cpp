int f(int n, int k, int l)
{
    if(n == 0) return 1;
    int res = 0;
    if(n >= k)
        res += f(n - k, k, l);
    if(n >= l)
        res += f(n - l, k, l);
    return res;
}

ll potmod(int b, int e, int m)
{
    if(e == 0) return 1;
    ll mitad = potmod(b, e / 2, m);
    if(e % 2 == 0)
        return mitad * mitad % m;
    return mitad * mitad % m * b % m;
}

int combinaciones(int n, int k)
{
    if(k == 0) return 1;
    if(n == 0) return 0;
    return combinaciones(n - 1, k - 1) + combinaciones(n - 1, k);
}

