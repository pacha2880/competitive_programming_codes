struct unionFind {
  vi p, si;
  unionFind(int n) : p(n, -1), si(n, 1) {}
  int findParent(int v) {
    if (p[v] == -1) return v;
    return p[v] = findParent(p[v]);
  }
  bool join(int a, int b) {
    a = findParent(a);
    b = findParent(b);
    if (a == b) return false;
    p[a] = b;
    si[b] += si[a];
    return true;
  }
};
