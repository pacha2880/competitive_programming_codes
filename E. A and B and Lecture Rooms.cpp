vi g[tam];
struct Node{ int de, par, go; };
vector<Node> t(tam);
int find(int nod, int de)
{
	while(t[nod].de > de)
		if(t[t[nod].go].de < de)
			nod = t[nod].par;
		else
			nod = t[nod].go;
	return nod;
}
void add(int par, int nod)
{
	t[nod] = {t[par].de + 1, par, 0};
	if(t[par].de - t[t[par].go].de == t[t[par].go].de - t[t[t[par].go].go].de)
		t[nod].go = t[t[par].go].go;
	else
		t[nod].go = par;
}
int lca(int a, int b)
{
	if(t[a].de < t[b].de) swap(a, b);
	a = find(a, t[b].de);
	while(a != b)
		if(t[a].go == t[b].go)
			a = t[a].par, b = t[b].par;
		else
			a = t[a].go, b = t[b].go;
	return a;
}
int siz[tam];
void dfs(int node, int par)
{
	siz[node] = 1;
	for(int x : g[node])
		if(x != par)
		{
			add(node, x);
			dfs(x, node);
			siz[node] += siz[x];
		}
}
