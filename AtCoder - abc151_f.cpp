// calcula el centro de masa de un poligono antihorario
point cen(point a, point b, point c) {
    point c1 = cent(a, b), c2 = cent(b, c);
    point p1 = a - c1;
    p1 = {-p1.y, p1.x};
    p1 = p1 + c1;
    point p2 = b - c2;
    p2 = {-p2.y, p2.x};
    p2 = p2 + c2;
    return intersec(line(p1, c1), line(p2, c2));
}
