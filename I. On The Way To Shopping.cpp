pst update(pst t, int b, int e, int pos, int val)
{
    if(b == e)
        return new st(t->val + val);
    int mid = (b + e) / 2;
    if(mid >= pos)
        return new st(update(t->l, b, mid, pos, val), t->r);
    return new st(t->l, update(t->r, mid + 1, e, pos, val));
}
