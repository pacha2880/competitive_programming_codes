int insertar(int x, int las)
{
    int trap = las, root = sz(trie);

    int ax = root;
    trie.pb(item());
    for(int i = 18; i > -1; i--)
	{
        // cout<<'%'<<i<<'\n';
        int neo = sz(trie);
        trie.pb(item());
        if(x & (1<<i))
        {
            trie[ax].un = neo;
            if(trap != -1)
            {
                trie[ax].ce = trie[trap].ce;
                trap = trie[trap].un;
            }
        }
        else
        {
            trie[ax].ce = neo;
            if(trap != -1)
            {
                trie[ax].un = trie[trap].un;
                trap = trie[trap].ce;
            }
        }
        ax = neo;
        trie[ax].can = 1;
        if(trap != -1)
            trie[ax].can += trie[trap].can;
	}
    return root;
}
