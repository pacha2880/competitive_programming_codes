bool les(int i, int j)
{
    if(top[i] == -1) return 0;
    if(top[j] == -1) return 1;
    int a = top[i], b = top[j];
    while(a > -1 && b > -1 && vis[i][a] == vis[j][b]) a--, b--;
    if(a == -1) return 1;
    if(b == -1) return 0;
    return vis[i][a] < vis[j][b];
}
